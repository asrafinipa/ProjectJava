package Array2DReview;

public class Array3 {
    public static void main(String[] args) {
        int []num={23,34,56,78,89};
        //iter for enhance loop
        for (int i : num) {
            System.out.println(i);
        }// fori for loop
        for (int i = 0; i < num.length ; i++) {
            System.out.println(num[i]);
        }
    }
}
